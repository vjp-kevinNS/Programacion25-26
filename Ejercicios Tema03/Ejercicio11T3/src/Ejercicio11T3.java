public class Ejercicio11T3 {

    public static void main(String[] args) {
        //Crear un bucle que muestre por pantalla el mensaje "hola" 6 veces, incrementando en 1 hasta 6
        int i; //Declaramos la variable i para utilizarla en el bucle for
        
        for (i=0; i<=6; i++){ //Mientras que i sea igual o menos a 6, incrementamos 1
            System.out.println("Hola1");
            System.out.println("Hola2");
            System.out.println("Hola3");
            System.out.println("Hola4");
            System.out.println("Hola5");
            System.out.println("Hola6");
        }
    }
}
