package ejercicio11t3holaconnumeros;

/**
 * Crea un programa en JAVA que, utilizando bucles, muestre por pantalla el
 * mensaje "Hola" seis veces acompañado por un numero que se incrementa cada
 * vez. 
 * • Muestra por pantalla el resultado de la siguiente forma: - Hola1 –
 * Hola2 – Hola3 – Hola4 – Hola5 – Hola6 -
 *
 * @author KevinNS
 */
public class Ejercicio11T3HolaConNumeros {

    public static void main(String[] args) {
        
        //Creamos el bucle for, esta vez la variable la crearemos dentro
        
        for (int i = 1; i <= 6; i++) {
            System.out.print("Hola" + i + " - ");
            
        }

    }

}
