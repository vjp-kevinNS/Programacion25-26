package ejercicio10t3hola5veces;

/**
 * Escribe un programa en JAVA que, utilizando bucles, muestre por pantalla el
 * mensaje "Hola" cinco veces.
 *
 * @author KevinNS
 */
public class Ejercicio10T3Hola5Veces {

    public static void main(String[] args) {
        
        //Creamos un bucle for
        
        int i; //Creamos esta variable para usarla en el bucle, aunque podemos 
               //crearla en el mismo for as√≠: for (int i = 0.....)
        for (i = 0; i < 5; i++) {
            System.out.println("Hola");
            
        }

    }

}
